package dto;
	

public class Piernas{

	private String pantalosnes;
	private String pans;
	public Piernas(String pantalosnes, String pans) {
		super();
		this.pantalosnes = pantalosnes;
		this.pans = pans;
	}
	public String getPantalosnes() {
		return pantalosnes;
	}
	public void setPantalosnes(String pantalosnes) {
		this.pantalosnes = pantalosnes;
	}
	public String getPans() {
		return pans;
	}
	public void setPans(String pans) {
		this.pans = pans;
	}
	
	

}	