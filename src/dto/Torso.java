package dto;


public class Torso{
	String camisa;
	String playera;
	
	
	public Torso(String camisa, String playera) {
		super();
		this.camisa = camisa;
		this.playera = playera;
	}


	public String getCamisa() {
		return camisa;
	}
	public void setCamisa(String camisa) {
		this.camisa = camisa;
	}
	public String getPlayera() {
		return playera;
	}
	public void setPlayera(String playera) {
		this.playera = playera;
	}
	
	
		
}





